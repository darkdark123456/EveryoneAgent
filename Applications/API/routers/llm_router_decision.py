import re
import json
from langchain_core.messages import HumanMessage, AIMessage, UsageMetadata
class RouteDecision(BaseModel):
    route: str
    reason: str
    summary: str
    
class LLMRouter:
    input_token: int = 0
    output_token: int = 0
    total_token: int = 0
    
    @staticmethod
    async def get_token_use()->Tuple[int,int,int]:
        return LLMRouter.input_token,LLMRouter.output_token,LLMRouter.total_token
    @staticmethod
    async def route(
        llm,
        query: str
    ) -> RouteDecision:
        
        result: AIMessage = await llm.ainvoke([("system",ROUTER_PROMPT),("human",query)])
        
        meta: dict=result.response_metadata
        usage: UsageMetadata | None = meta.get("token_usage", {})
        assert usage is not None
        
        LLMRouter.input_token=usage.get("input_tokens",0)
        LLMRouter.output_token=usage.get("output_tokens",0)
        LLMRouter.total_token=usage.get("total_tokens",0)
        
        content: str = result.content.strip() # type: ignore
        json_match = re.search(r"\{[\s\S]*?\}", content)
        if not json_match:
            return RouteDecision(route="UNKNOWN", reason="",summary="")
        try:
            json_data = json.loads(json_match.group())
            return RouteDecision(**json_data)
        except Exception:
            return RouteDecision(route="UNKNOWN", reason="",summary="")

 